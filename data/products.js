export const getCategoryProducts = (category) => {
  const products = allProducts[category] || []
  // Apply product edits from localStorage
  let finalProducts = []
  if (typeof window !== 'undefined') {
    const edits = JSON.parse(localStorage.getItem('productEdits') || '{}')
    finalProducts = products.map(product => ({
      ...product,
      ...edits[product.id]
    }))
    
    // Add custom products for this category
    const customProducts = JSON.parse(localStorage.getItem('customProducts') || '[]')
    const categoryCustomProducts = customProducts
      .filter(p => p.category === category)
      .map(product => ({
        ...product,
        ...edits[product.id]
      }))
    finalProducts = [...finalProducts, ...categoryCustomProducts]
  } else {
    finalProducts = products
  }
  return finalProducts
}

export const getProductById = (id) => {
  // Check custom products first
  if (typeof window !== 'undefined') {
    const customProducts = JSON.parse(localStorage.getItem('customProducts') || '[]')
    const customProduct = customProducts.find(p => p.id === id)
    if (customProduct) {
      const edits = JSON.parse(localStorage.getItem('productEdits') || '{}')
      return {
        ...customProduct,
        ...edits[id]
      }
    }
  }
  
  // Check regular products
  for (const category in allProducts) {
    const product = allProducts[category].find(p => p.id === id)
    if (product) {
      // Apply product edits from localStorage
      if (typeof window !== 'undefined') {
        const edits = JSON.parse(localStorage.getItem('productEdits') || '{}')
        return {
          ...product,
          ...edits[id]
        }
      }
      return product
    }
  }
  return null
}
